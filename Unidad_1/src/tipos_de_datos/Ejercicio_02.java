package tipos_de_datos;

public class Ejercicio_02 {

	public static void main(String[] args) {

		float pan = 0.87f;
		float gramoQueso = 13.10f;

		float total = pan + (150 * gramoQueso / 1000);

		System.out.println("Un bocadillo de 150 gramos de queso cuesta : " + total + " euros.");

		System.out.println("\nFin del programa.");

	}

}
