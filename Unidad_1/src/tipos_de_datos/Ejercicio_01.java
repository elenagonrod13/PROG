package tipos_de_datos;

public class Ejercicio_01 {

	public static void main(String[] args) {

		int pobAsturias = 104200;
		int pobMadrid = 6464000;
		long mundo = 7263370459L;

		System.out.println("Poblacion de Asturias : " + pobAsturias + " personas.");
		System.out.println("Poblacion de Madrid : " + pobMadrid + " personas.");
		System.out.println("Poblacion mundial : " + mundo + " personas.");

		System.out.println("\nFin del programa.");

	}

}
